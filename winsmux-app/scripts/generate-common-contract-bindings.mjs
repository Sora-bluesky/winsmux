import assert from "node:assert/strict";
import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { pathToFileURL } from "node:url";
import ts from "typescript";

async function loadModelCapabilitiesModule() {
  const sourcePath = path.resolve("src/modelCapabilities.ts");
  const source = await readFile(sourcePath, "utf8");
  const transpiled = ts.transpileModule(source, {
    compilerOptions: {
      module: ts.ModuleKind.ESNext,
      target: ts.ScriptTarget.ES2020,
    },
    fileName: sourcePath,
  });

  const tempDir = await mkdtemp(path.join(os.tmpdir(), "winsmux-common-contract-bindings-"));
  const modulePath = path.join(tempDir, "modelCapabilities.mjs");
  await writeFile(modulePath, transpiled.outputText, "utf8");
  try {
    return await import(pathToFileURL(modulePath).href);
  } finally {
    await rm(tempDir, { recursive: true, force: true });
  }
}

function quotePowerShellString(value) {
  return `'${String(value).replaceAll("'", "''")}'`;
}

function formatPowerShellArray(values) {
  return `@(${values.map(quotePowerShellString).join(", ")})`;
}

function renderPowerShellBinding(commonContractPackage) {
  const vocabularies = commonContractPackage.vocabularies;
  const entries = Object.entries(vocabularies).map(([name, vocabulary]) => {
    assert.ok(Array.isArray(vocabulary.values), `Vocabulary ${name} must include values`);
    return `    ${name} = ${formatPowerShellArray(vocabulary.values)}`;
  });

  return `# <auto-generated>
# Source: winsmux-app/src/modelCapabilities.ts commonContractPackage
# Generated by winsmux-app/scripts/generate-common-contract-bindings.mjs.
# Do not edit by hand.

$script:BridgeCommonContractPackageVersion = ${quotePowerShellString(commonContractPackage.version)}
$script:BridgeCommonContractVocabularies = @{
${entries.join("\n")}
}

function Get-BridgeCommonContractVocabularyValues {
    param([Parameter(Mandatory = $true)][string]$Name)

    if (-not $script:BridgeCommonContractVocabularies.ContainsKey($Name)) {
        throw "Unknown common contract vocabulary '$Name'."
    }

    return @($script:BridgeCommonContractVocabularies[$Name])
}

function Test-BridgeCommonContractVocabularyValue {
    param(
        [Parameter(Mandatory = $true)][string]$Name,
        [AllowNull()][string]$Value
    )

    if ([string]::IsNullOrWhiteSpace($Value)) {
        return $false
    }

    $normalized = $Value.Trim().ToLowerInvariant()
    return ($normalized -in @(Get-BridgeCommonContractVocabularyValues -Name $Name))
}

function Get-BridgeCommonContractSupportedValuesText {
    param([Parameter(Mandatory = $true)][string]$Name)

    return ((Get-BridgeCommonContractVocabularyValues -Name $Name) -join ', ')
}
`;
}

function normalizeLineEndings(value) {
  return value.replaceAll("\r\n", "\n");
}

const args = new Set(process.argv.slice(2));
const checkOnly = args.has("--check");
const { commonContractPackage } = await loadModelCapabilitiesModule();
const outputPath = path.resolve("..", "winsmux-core", "scripts", "common-contract.generated.ps1");
const rendered = renderPowerShellBinding(commonContractPackage);

if (checkOnly) {
  const current = await readFile(outputPath, "utf8");
  assert.equal(normalizeLineEndings(current), rendered, `${outputPath} is stale; run npm run generate:common-contract-bindings`);
  console.log("common-contract-bindings: ok");
} else {
  await writeFile(outputPath, rendered, "utf8");
  console.log(`common-contract-bindings: wrote ${path.relative(process.cwd(), outputPath)}`);
}
